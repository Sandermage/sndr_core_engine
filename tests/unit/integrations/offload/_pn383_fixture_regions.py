    "                num_offloadable_tokens = min(\n"
    "                    num_offloadable_tokens, req.num_prompt_tokens\n"
    "                )\n"
    "\n"
    "            # Filter out blocks skipped due to sliding window attention / SSM\n"
    "            # or unreachable by the load path's alignment constraints.\n"
    "            new_offload_keys: list[OffloadKey] = []\n"
    "            for group_config, group_state in zip(\n"
