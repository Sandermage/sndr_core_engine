const DICT: Record<Lang, Dict> = { en: EN, ru: RU };

export function t(lang: Lang, key: string, fallback?: string): string {
  return DICT[lang]?.[key] ?? DICT.en[key] ?? fallback ?? key;
}

// ── Source-string translation (tr) ──────────────────────────────────────────
// For screens that were authored in English without semantic keys, `tr("…")`
// looks the English source string up in RU_BY_EN and returns the Russian when
// the UI is in Russian, falling back to the English passthrough otherwise. This
// lets a component be localized by wrapping its literals — no key invention, no
// `lang` threading. Reactivity: a module-level `_trLang` mirror is updated on
// the same lang-change event `useLang()` listens to, so the App root re-renders
// and every `tr()` in the tree recomputes against the new language.
let _trLang: Lang = getLang();
if (typeof window !== "undefined") {
  const sync = () => { _trLang = getLang(); };
  window.addEventListener(EVT, sync);
  window.addEventListener("storage", sync);
}

export function tr(s: string): string {
  if (_trLang === "en") return s;
  return RU_BY_EN[s] ?? s;
}

